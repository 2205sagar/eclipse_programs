package com.xworkz.constructor;

public class Swap {
 
	  
		 public static void main(String a[])
		    {
		        int x = 22;
		        int y = 18;
		        x = x + y;
		        y = x - y;
		        x = x - y;
		        System.out.println("After swapping:"
		                           + " x = " + x + ", y = " + y);
		    }

}
